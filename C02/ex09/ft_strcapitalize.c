void	func(char *str)
{
	if (!((*(str - 1) >= 48 && *(str - 1) <= 57)
			|| (*(str - 1) >= 97 && *(str - 1) <= 122)
			|| (*(str - 1) >= 65 && *(str - 1) <= 90)))
	{
		if (*str >= 97 && *str <= 122)
			*str -= 32;
	}
}

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		i++;
	}
	i = 0;
	if (str[i] >= 97 && str[i] <= 122)
		str[i] -= 32;
	i++;
	while (str[i])
	{
		func(&str[i]);
		i++;
	}
	return (str);
}
